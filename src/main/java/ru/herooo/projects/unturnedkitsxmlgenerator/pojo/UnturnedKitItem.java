package ru.herooo.projects.unturnedkitsxmlgenerator.pojo;

public class UnturnedKitItem {
    private String name;
    private int amount;
    private String mod;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public String getMod() {
        return mod;
    }

    public void setMod(String mod) {
        this.mod = mod;
    }
}
